    /**
     * This fragment shows data and sync preferences only. It is used when the
     * activity is showing a two-pane settings UI.
     */
    @TargetApi(Build.VERSION_CODES.HONEYCOMB)
    public static class DataSyncPreferenceFragment extends PreferenceFragment implements Preference.OnPreferenceClickListener{
        //TODO: Save selected BLE device and persist data


        public static final String KEY_BLE_SYNC = "ble_device_scan";
        private Handler mHandler;

        ListPreference mListPreference;
        public ArrayList<CharSequence> deviceNames;
        public ArrayList<CharSequence> deviceAddresses;
        public ArrayList<BluetoothDevice> devices;
        private BluetoothAdapter mBluetoothAdapter;
        private BluetoothLeScanner mBluetoothLeScanner;
        private boolean mScanning;
        private static final int REQUEST_ENABLE_BT = 1;
        // Stops scanning after 10 seconds.
        private static final long SCAN_PERIOD = 10000;
        @Override
        public void onCreate(Bundle savedInstanceState) {
            super.onCreate(savedInstanceState);
            addPreferencesFromResource(R.xml.pref_data_sync);
            setHasOptionsMenu(true);

            // Bind the summaries of EditText/List/Dialog/Ringtone preferences
            // to their values. When their values change, their summaries are
            // updated to reflect the new value, per the Android Design
            // guidelines.
//            bindPreferenceSummaryToValue(findPreference("sync_frequency"));



            // New Code

            mHandler = new Handler();
            mListPreference = (ListPreference) findPreference("ble_device_scan");
            deviceNames = new ArrayList<CharSequence>();
            deviceAddresses = new ArrayList<CharSequence>();
            devices = new ArrayList<BluetoothDevice>();
            CharSequence[] sampleEntries = {"No devices found"};
            mListPreference.setEntries(sampleEntries);
            mListPreference.setEntryValues(sampleEntries);

            final BluetoothManager bluetoothManager = (BluetoothManager) getActivity().getSystemService(Context.BLUETOOTH_SERVICE);
            mBluetoothAdapter = bluetoothManager.getAdapter();
            mBluetoothLeScanner = mBluetoothAdapter.getBluetoothLeScanner();
        }

        @Override
        public boolean onOptionsItemSelected(MenuItem item) {
            int id = item.getItemId();
            if (id == android.R.id.home) {
                startActivity(new Intent(getActivity(), SettingsActivity.class));
                return true;
            }
            return super.onOptionsItemSelected(item);
        }




        private void scanLeDevice(final boolean enable) {
            if (enable) {
                // Stops scanning after a pre-defined scan period.
                mHandler.postDelayed(new Runnable() {
                    @Override
                    public void run() {
                        mScanning = false;
                        mBluetoothLeScanner.stopScan(mLeScanCallback);
                    }
                }, SCAN_PERIOD);

                mScanning = true;
                mBluetoothLeScanner.startScan(mLeScanCallback);
            } else {
                mScanning = false;
                mBluetoothLeScanner.stopScan(mLeScanCallback);
            }
        }

        private ScanCallback mLeScanCallback =
                new ScanCallback() {

                    @Override
                    public void onScanResult(int callbackType, ScanResult result) {
                        Log.d(getClass().getName(), "Found device");
                        devices.add(result.getDevice());
                        deviceNames.add(result.getDevice().getName() + " - " + result.getDevice().getAddress());
                        deviceAddresses.add(result.getDevice().getAddress());
                        mListPreference.setEntryValues(deviceAddresses.toArray(new CharSequence[10]));
                        mListPreference.setEntries(deviceNames.toArray(new CharSequence[10]));

                    }
                };

        @Override
        public boolean onPreferenceClick(Preference preference) {

            if (preference.getKey().equals(KEY_BLE_SYNC)) {
                if(mBluetoothAdapter == null) {
                    Toast.makeText(getActivity(), R.string.no_ble_error_message, Toast.LENGTH_SHORT);
                } else {
                    Log.d(this.getClass().getName(), "BLE works, now scanning");
                    scanLeDevice(true);
                }
            }
            return false;
        }
    }
